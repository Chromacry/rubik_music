package com.tp.musicapp_project.GenerateData;

public class SongData {
    public String id, songTitle, songArtist, songGenre, songCategory, songFileLink, songImageLink;

    public SongData(){

    }

    public String getId() {
        return id;
    }

    public String getSongTitle() {
        return songTitle;
    }

    public String getSongArtist() {
        return songArtist;
    }

    public String getSongGenre() {
        return songGenre;
    }

    public String getSongCategory() {
        return songCategory;
    }

    public String getSongFileLink() {
        return songFileLink;
    }

    public String getSongImageLink() {
        return songImageLink;
    }
}
